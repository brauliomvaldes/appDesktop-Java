    void llenarTablaConPersonas(){
        modelTable = new DefaultTableModel(null,titulos);
        ControladorPersona cpersonas = new ControladorPersona();
        ArrayList<Personas> persona;
        persona = cpersonas.consultarTodo();
        if(persona!=null){
            int largo=persona.size(),tp,e;
            String dd,mm,aa;
            while (largo>0){
                //extrae datos del arraylist para poblar tabla personas             
                campos[0]=String.valueOf(persona.get(largo-1).getId_persona_rut())+"-"+
                                   persona.get(largo-1).getDv_persona();
                
                campos[1]=persona.get(largo-1).getNombre()+" "+persona.get(largo-1).getApaterno()+
                            " "+persona.get(largo-1).getAmaterno();                
                dd=String.valueOf(persona.get(largo-1).getDia_contrato());
                mm=String.valueOf(persona.get(largo-1).getMes_contrato());
                aa=String.valueOf(persona.get(largo-1).getYear_contrato());
                campos[2]=dd+"/"+mm+"/"+aa;
                aa=String.valueOf(persona.get(largo-1).getDia_incorporado());
                mm=String.valueOf(persona.get(largo-1).getMes_incorporado());
                aa=String.valueOf(persona.get(largo-1).getYear_incorporado());
                campos[3]=dd+"/"+mm+"/"+aa;
                tp=persona.get(largo-1).getTipo_persona();
                if(tp==0){
                    campos[4]="Cliente";
                }else{
                    if(tp==1){
                        campos[4]="Dependiente";
                    }else{
                        campos[4]="Cliente y Dependiente";
                    }
                }
                e=persona.get(largo-1).getEstado();
                if(e==0){
                    campos[5]="Eliminado";
                }else{
                    if(e==1){
                        campos[5]="Vigente";
                    }else{
                        campos[5]="Bloqueado por arriendo en mora";
                    }
                }
                modelTable.addRow(campos);
                largo--;
            }
         jTablePersonas.removeAll();
         jTablePersonas.setModel(modelTable);
                 //ALINEA CELDAS TABLAS
         DefaultTableCellRenderer alinear = new DefaultTableCellRenderer();
         alinear.setHorizontalAlignment(SwingConstants.RIGHT);//.LEFT .RIGHT .CENTER
         jTablePersonas.getColumnModel().getColumn(0).setCellRenderer(alinear);
         jTablePersonas.getColumnModel().getColumn(1).setCellRenderer(alinear);
         jTablePersonas.getColumnModel().getColumn(2).setCellRenderer(alinear);
         jTablePersonas.getColumnModel().getColumn(3).setCellRenderer(alinear);
         jTablePersonas.getColumnModel().getColumn(4).setCellRenderer(alinear);
         jTablePersonas.getColumnModel().getColumn(5).setCellRenderer(alinear);
        }
    }

    
    
    
    
