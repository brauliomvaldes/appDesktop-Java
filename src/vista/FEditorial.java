    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe ingresa editorial", "Error de Ingreso", 1);
        }else{
            String nombre = txtNombre.getText();      
            ControladorEditorial c = new ControladorEditorial();
            //busca el editorial
            Editorial editorial = c.leer(nombre); //recupera objeto editorial si lo encuentra
            if (editorial != null) {
                //busca la editorial si esta utilizado en algún libro
                ControladorLibros lib = new ControladorLibros();
                //JOptionPane.showMessageDialog(null, "editorial a eliminar"+autores.getId_autor(), "Ingreso Válido",0);
                Libros libros = lib.leerEditorialEnLibro(editorial.getId_editorial()); 
                //recupera objeto libro si encuentra el autor
                if(libros!=null){
                    JOptionPane.showMessageDialog(null, "No es posible cambiar estado, esta vinculado a un libro", "no procesado",1);
                }else{
                    if(c.cambiaEstado(editorial.getId_editorial(),!(editorial.isEstado()))){
                        if(editorial.isEstado()){
                            JOptionPane.showMessageDialog(null, "Se cambio estado a "+"borrado", "Procesado",1);
                        }else{
                            JOptionPane.showMessageDialog(null, "Se cambio estado a "+"vigente", "Procesado",1);
                        }
                        poblarEditorial();
                       }
                    }
            }else{
                JOptionPane.showMessageDialog(null, "Editorial no existe", "Error de proceso", 1);
            }
            txtNombre.setText("");
        }  
    }//GEN-LAST:event_btnBorrarActionPerformed

