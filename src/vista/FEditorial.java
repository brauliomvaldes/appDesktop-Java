    private void btnAgregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregaActionPerformed
        // TODO add your handling code here:
     
        // aqui agrega editoriales
        if (txtNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe ingresa editorial", "Error de Ingreso", 1);
        }else{
            String nombre = txtNombre.getText();      
            ControladorEditorial c = new ControladorEditorial();
            Editorial editorial = c.leer(nombre); //recupera objeto editorial si lo encuentra
            if (editorial != null) {
                JOptionPane.showMessageDialog(null, "Edtorial existente", "Error de Ingreso", 1);
            }else{
                if(c.agregar(nombre)){
                    JOptionPane.showMessageDialog(null, "Editorial creada exitosamente", "Ingreso Válido",1);
                    poblarEditorial();
                }else{
                    JOptionPane.showMessageDialog(null, "Editorial no fue creada", "Ingreso No Válido",1);
                }
            }
            txtNombre.setText("");
        }
    }//GEN-LAST:event_btnAgregaActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
                
        if (txtNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe ingresa editorial", "Error de Ingreso", 1);
        }else{
            String nombre = txtNombre.getText();      
            ControladorEditorial c = new ControladorEditorial();
            Editorial editorial = c.leer(nombre); //recupera objeto editorial si lo encuentra
            if (editorial != null) {
                JOptionPane.showMessageDialog(null, "Moficación ya existe, no se registró cambio", "Error", 1);
            }else{
                // se modifica la editorial
                String e = cmbEditorial.getItemAt(cmbEditorial.getSelectedIndex());
                int id_editorial = Integer.parseInt(e.substring(e.indexOf("(") + 1, e.indexOf(")")));
               
                Editorial edt = new Editorial(id_editorial,nombre,true);
                if(c.modificar(edt)){
                        JOptionPane.showMessageDialog(null, "Editoial modificada exitosamente", "Ingreso Válido",1);
                        poblarEditorial();
                    }else{
                        JOptionPane.showMessageDialog(null, "Editorial no fue modificado", "Modificación No Válida",1);
                    }
            }
        } 
    
    }//GEN-LAST:event_btnModificarActionPerformed


    private void poblarEditorial(){
        ControladorEditorial c = new ControladorEditorial();
        ArrayList<Editorial> editorial = c.consultarTodo();
        comboEditorial = new DefaultComboBoxModel();
        String estado;
        for (Editorial e : editorial) {
          if(e.isEstado()){
             estado = "vigente"; 
          }else{
             estado = "borrado"; 
          }  
          comboEditorial.addElement(e.getNombre()+"("+e.getId_editorial()+")"+"["+estado+"]");
          cmbEditorial.setModel(comboEditorial);
        }
    }
    
       
